package net.skhu.dto;

public class Department {
	int id;
	int departmentName;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(int departmentName) {
		this.departmentName = departmentName;
	}
}
