package net.skhu.dto;

public class SubstitutionSubject {
	int studentId;
	String abolitionSubject;
	String abolitionSubjectName;
	String substitutionSubject;
	String substitutionSubjectName;
	int departmentId;
	String departmentName;
	
	public int getStudentId() {
		return studentId;
	}
	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}
	
	public String getAbolitionSubject() {
		return abolitionSubject;
	}
	public void setAbolitionSubject(String abolitionSubject) {
		this.abolitionSubject = abolitionSubject;
	}
	public String getAbolitionSubjectName() {
		return abolitionSubjectName;
	}
	public void setAbolitionSubjectName(String abolitionSubjectName) {
		this.abolitionSubjectName = abolitionSubjectName;
	}
	public String getSubstitutionSubject() {
		return substitutionSubject;
	}
	public void setSubstitutionSubject(String substitutionSubject) {
		this.substitutionSubject = substitutionSubject;
	}
	public String getSubstitutionSubjectName() {
		return substitutionSubjectName;
	}
	public void setSubstitutionSubjectName(String substitutionSubjectName) {
		this.substitutionSubjectName = substitutionSubjectName;
	}
	public int getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(int departmentId) {
		this.departmentId = departmentId;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
}
